        <h2>Je suis Sébastien JOFFRE et je chasse les dolmens depuis 2018.</h2>
        <p>C'est à l'aube de mes 40 ans, non sans une réelle émotion, que j'ai découvert l'existence de ce
            patrimoine méconnu mais pourtant incroyable que représentent les Mégalithes.
            </p>
        <p>
            Depuis mon enfance, j'éprouvais une grande curiosité pour la Préhistoire, notamment pour les grottes
            ornées. Pourtant, ce n'est que bien plus tard que les dolmens, menhirs et autres tumulus ont croisé mon
            chemin.</p>
        <p>Mon aventure <strong>Chasseur de Dolmens</strong> débuta au Dolmen des Sept Chemins, situé à Bougon dans les
            Deux-Sèvres, à seulement 30 minutes de chez moi. Découvrir ce monument érigé par des hommes et
            des femmes il y a 6000 ans, posé là, au bord d'une route départementale, en pleine campagne et en
            libre accès, m'a totalement fasciné. Je vécus cet instant comme un immense privilège.</p>
        <p>Dès lors, le désir de découvrir d'autres sites me saisit, et la quête devint insatiable. Au fil de mes
            recherches, je réalisais que ce patrimoine se trouvait partout autour de moi : au sud des Deux-Sèvres,
            au nord, dans la Vienne, en Charente, en Vendée !!</p>
        <p><strong>Un véritable patrimoine à part entière se révélait à moi. Celui-ci allait bouleverser mon existence.</strong></p>
        <p>Au bout d'un peu plus d'un an, en août 2019, disposant de pas mal de photographies prises avec mon
            téléphone portable, je décidai de créer ma page Facebook pour publier tous mes trophées
            photographiques de chasse. Un an plus tard, je lançai ma chaîne Youtube pour mettre en valeur, via des
            vidéos musicales, le patrimoine mégalithique du Grand Ouest..</p>
        <p>Suite à un article de la Nouvelle République, j'ai été contacté par l'Espace Mendès France (Centre de
            culture scientifique, technique et industrielle) de Poitiers qui m'a orienté vers le Programme d'Animation
            d'Initiatives de Culture Scientifique, soutenu par la Région Nouvelle Aquitaine, qui m'a permis d'être aidé
            financièrement, deux années de suite, dans l'organisation d'expositions itinérantes en Deux Sèvres,
            Vienne et Charente afin d'y présenter un aperçu du patrimoine mégalithique de ces départements.
            </p>
        <p>Ce projet ambitieux a profondément transformé ma manière de photographier. Il ne s'agissait plus de
            présenter un monument à travers plusieurs photographies mais de mettre en lumière une région, le
            Poitou, à travers ses plus beaux sites mégalithiques. Grâce à ce projet, j'ai appris la photographie.</p>
        <p><strong>Grâce aux mégalithes, je suis devenu photographe</strong></p>
